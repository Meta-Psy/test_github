# Математические операторы:
x = (2+2-2)*2/2                         # Есть все классические операторы. Все работает по законам математики
y = 3//2+3%2                            # //: Деление без остатка (5//2 = 2). %: Остаток от деления (5%2 = 1)
z = 3**3                                # **: Возведение в степень

print(string*z)                         # Можно использовать математические операторы на разные типы данных (string и integer)
print(string+string)                    # integer + integer
print(string, z)                        # Помимо операторов, можно сделать перечисление при помощи ","

# Условные операторы
a = float(input('Введите число, чтобы выяснить больше ли оно 100: '))              # Для введения любого числа пользователем
if a > 100:                             # If - начианющий условный оператор. Без него не запустить остальные операторы. В конце ":", обязательно
    print('a - больше ста')             # Код-действие всегда печатать после "Tab"
elif a < 100:                           # Elif - если "If" - False, то срабатывает оператор "elif". "Elif" может быть бесконечное количество
    print('a - меньше ста')
else:                                   # Else - если все "Elif" False, то включается оператор "Else". Не требует указания значений
    print('a - и есть 100')

# Операторы сравнения
print(bool(z > x))                      # Знак больше
