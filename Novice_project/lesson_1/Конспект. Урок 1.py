print(bool(x == x))                     # Знак равно. Т.к. обычное равно занято оформлением переменных, то для операторов сравнения, пришлось использовать "=="
print(bool(x >= x))                     # Знак больше или равно
print(bool(x <= x))                     # Знак меньше или равно
print(bool(x != y))                     # Знак неравенства
